export function getSubcategories(category?: string): Option[] {
  if (!category) return [];

  const subcategories =
    productCategories
      .find((c) => c.title === category)
      ?.subcategories.map((s) => ({
        label: s.title,
        value: s.slug,
      })) ?? [];

  return subcategories;
}

export function getSubcategory(slug: string): Subcategory | undefined {
  return productCategories
    .flatMap((c) => c.subcategories)
    .find((s) => s.slug === slug);
}
